#!/usr/bin/env node
// Regenerate src/endpoints.generated.ts from the live HasData schema
// at /apis and /apis/<slug>. Run: `npm run generate`.

import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

// Hardcoded — this dev-only generator only ever talks to the official HasData
// catalog. No env-var override, so the repo offers no knob for redirecting
// schema fetches to an attacker-controlled host.
const BASE = "https://api.hasdata.com";
const OUT = join(
  dirname(fileURLToPath(import.meta.url)),
  "..",
  "src",
  "endpoints.generated.ts",
);

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${url} → HTTP ${res.status}`);
  return res.json();
}

function stripUrl(fullUrl) {
  const u = new URL(fullUrl);
  return u.pathname;
}

function cleanDescription(desc) {
  if (!desc) return "";
  return desc.replace(/\s+/g, " ").trim();
}

// HasData uses PHP-style bracketed names on the wire: `price[min]`, `homeTypes[]`.
// We expose an ergonomic camelCase key to the LLM and remember the original wireName
// so the client can translate at request time.
//   `homeTypes[]`    → displayName "homeTypes", wireName "homeTypes[]"
//   `price[min]`     → displayName "priceMin",  wireName "price[min]"
//   `yearBuilt[max]` → displayName "yearBuiltMax", wireName "yearBuilt[max]"
//   `keyword`        → displayName "keyword",  wireName "keyword"
function deriveDisplayName(rawKey) {
  if (rawKey.endsWith("[]")) return rawKey.slice(0, -2);
  const m = rawKey.match(/^([^[]+)\[([^\]]+)\]$/);
  if (m) {
    const [, base, sub] = m;
    return base + sub.charAt(0).toUpperCase() + sub.slice(1);
  }
  return rawKey;
}

// Reduce a HasData property to a compact ParamSpec we'll emit.
function normalizeProp(rawKey, raw) {
  const spec = {
    type: raw.type ?? "string",
    description: cleanDescription(raw.description ?? raw.title ?? ""),
    wireName: rawKey,
  };

  if (raw.default !== undefined) spec.default = raw.default;

  // enum can be an array of primitives or sit under oneOf: [{const,title}, ...]
  let enumVals;
  if (Array.isArray(raw.enum)) {
    enumVals = raw.enum;
  } else if (Array.isArray(raw.oneOf) && raw.oneOf.every((o) => "const" in o)) {
    enumVals = raw.oneOf.map((o) => o.const);
  }
  if (enumVals && enumVals.length > 0) spec.enum = enumVals;

  if (raw.type === "array" && raw.items) {
    const items = { type: raw.items.type ?? "string" };
    if (Array.isArray(raw.items.enum)) items.enum = raw.items.enum;
    else if (
      Array.isArray(raw.items.oneOf) &&
      raw.items.oneOf.every((o) => "const" in o)
    ) {
      items.enum = raw.items.oneOf.map((o) => o.const);
    }
    spec.items = items;
  }

  if (raw.type === "object") {
    spec.additionalProperties = true;
  }

  return spec;
}

async function main() {
  console.error(`Fetching catalog from ${BASE}/apis ...`);
  const categories = await fetchJson(`${BASE}/apis`);

  const apis = categories
    .flatMap((cat) =>
      (cat.apis ?? []).map((a) => ({ ...a, categoryTitle: cat.title })),
    )
    .filter((a) => a.mode !== "deprecated");

  console.error(`Found ${apis.length} live endpoints; fetching details ...`);

  const entries = [];
  for (const api of apis) {
    process.stderr.write(`  ${api.slug} ... `);
    let detail;
    try {
      detail = await fetchJson(`${BASE}/apis/${api.slug}`);
    } catch (err) {
      console.error(`SKIP (${err.message})`);
      continue;
    }
    const schema = detail.schema ?? {};
    const method = (schema.method ?? "GET").toUpperCase();
    const path = schema.url ? stripUrl(schema.url) : null;
    if (!path) {
      console.error(`SKIP (no URL)`);
      continue;
    }
    const rawRequired = Array.isArray(schema.required) ? schema.required : [];
    const properties = {};
    for (const [rawKey, raw] of Object.entries(schema.properties ?? {})) {
      const display = deriveDisplayName(rawKey);
      properties[display] = normalizeProp(rawKey, raw);
    }
    const required = rawRequired.map(deriveDisplayName);

    entries.push({
      slug: api.slug,
      title: api.title,
      description: cleanDescription(api.description ?? detail.description ?? ""),
      category: api.categoryTitle ?? detail.category?.title ?? "",
      method,
      path,
      cost: schema.cost ?? api.price ?? 0,
      required,
      properties,
    });
    console.error("ok");
  }

  entries.sort((a, b) => a.slug.localeCompare(b.slug));

  const header = `// @generated by scripts/generate-endpoints.mjs — do not edit by hand.
// Run \`npm run generate\` to refresh from ${BASE}/apis.

export interface ParamSpec {
  type: "string" | "integer" | "number" | "boolean" | "array" | "object";
  description?: string;
  enum?: (string | number | boolean)[];
  items?: { type: string; enum?: (string | number | boolean)[] };
  default?: unknown;
  additionalProperties?: boolean;
  /**
   * The parameter name HasData expects on the wire. Differs from the
   * schema key when the upstream API uses PHP-style brackets
   * (e.g. wire "price[min]" is exposed as schema key "priceMin").
   */
  wireName: string;
}

export interface EndpointSpec {
  slug: string;
  title: string;
  description: string;
  category: string;
  method: "GET" | "POST";
  path: string;
  cost: number;
  required: string[];
  properties: Record<string, ParamSpec>;
}

`;

  const body = `export const ENDPOINTS = ${JSON.stringify(
    Object.fromEntries(entries.map((e) => [e.slug, e])),
    null,
    2,
  )} as const satisfies Record<string, EndpointSpec>;

export type EndpointSlug = keyof typeof ENDPOINTS;

export const ENDPOINT_SLUGS = Object.keys(ENDPOINTS) as EndpointSlug[];
`;

  writeFileSync(OUT, header + body);
  console.error(`\nWrote ${OUT} (${entries.length} endpoints).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});